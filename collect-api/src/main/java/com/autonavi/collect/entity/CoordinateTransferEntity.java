package com.autonavi.collect.entity;

public class CoordinateTransferEntity {
	private Integer fromGpsSys;
	private Integer toGpsSys;
	private double x;
	private double y;
	public Integer getFromGpsSys() {
		return fromGpsSys;
	}
	public void setFromGpsSys(Integer fromGpsSys) {
		this.fromGpsSys = fromGpsSys;
	}
	public Integer getToGpsSys() {
		return toGpsSys;
	}
	public void setToGpsSys(Integer toGpsSys) {
		this.toGpsSys = toGpsSys;
	}
	public double getX() {
		return x;
	}
	public void setX(double x) {
		this.x = x;
	}
	public double getY() {
		return y;
	}
	public void setY(double y) {
		this.y = y;
	}
	
	

}
